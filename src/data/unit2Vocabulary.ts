/**
 * Helper function to get vocabulary by category
 */
export function getUnit2VocabularyByCategory(category: string): typeof unit2Vocabulary {
  return unit2Vocabulary.filter(item => item.category === category);
}

/**
 * Helper function to get vocabulary by tag
 */
export function getUnit2VocabularyByTag(tag: string): typeof unit2Vocabulary {
  return unit2Vocabulary.filter(item => item.tags?.includes(tag));
}

/**
 * Helper function to search vocabulary
 */
export function searchUnit2Vocabulary(searchTerm: string): typeof unit2Vocabulary {
  const term = searchTerm.toLowerCase();
  return unit2Vocabulary.filter(item =>
    item.hiragana.includes(term) ||
    item.kanji?.includes(term) ||
    item.vietnamese.toLowerCase().includes(term)
  );
}

