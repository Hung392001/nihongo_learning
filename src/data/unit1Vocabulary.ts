/**
 * Helper function to get vocabulary by category
 */
export function getVocabularyByCategory(category: string): typeof unit1Vocabulary {
  return unit1Vocabulary.filter(item => item.category === category);
}

/**
 * Helper function to get vocabulary by tag
 */
export function getVocabularyByTag(tag: string): typeof unit1Vocabulary {
  return unit1Vocabulary.filter(item => item.tags?.includes(tag));
}

/**
 * Helper function to search vocabulary
 */
export function searchVocabulary(searchTerm: string): typeof unit1Vocabulary {
  const term = searchTerm.toLowerCase();
  return unit1Vocabulary.filter(item =>
    item.hiragana.includes(term) ||
    item.kanji?.includes(term) ||
    item.vietnamese.toLowerCase().includes(term)
  );
}

