/**
 * Helper function to get vocabulary by category
 */
export function getUnit3VocabularyByCategory(category: string): typeof unit3Vocabulary {
  return unit3Vocabulary.filter(item => item.category === category);
}

/**
 * Helper function to get vocabulary by tag
 */
export function getUnit3VocabularyByTag(tag: string): typeof unit3Vocabulary {
  return unit3Vocabulary.filter(item => item.tags?.includes(tag));
}

/**
 * Helper function to search vocabulary
 */
export function searchUnit3Vocabulary(searchTerm: string): typeof unit3Vocabulary {
  const term = searchTerm.toLowerCase();
  return unit3Vocabulary.filter(item =>
    item.hiragana.includes(term) ||
    item.kanji?.includes(term) ||
    item.vietnamese.toLowerCase().includes(term)
  );
}

