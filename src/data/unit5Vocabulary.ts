/**
 * Helper function to get vocabulary by category
 */
export function getUnit5VocabularyByCategory(category: string): typeof unit5Vocabulary {
  return unit5Vocabulary.filter(item => item.category === category);
}

/**
 * Helper function to get vocabulary by tag
 */
export function getUnit5VocabularyByTag(tag: string): typeof unit5Vocabulary {
  return unit5Vocabulary.filter(item => item.tags?.includes(tag));
}

/**
 * Helper function to search vocabulary
 */
export function searchUnit5Vocabulary(searchTerm: string): typeof unit5Vocabulary {
  const term = searchTerm.toLowerCase();
  return unit5Vocabulary.filter(item =>
    item.hiragana.includes(term) ||
    item.kanji?.includes(term) ||
    item.vietnamese.toLowerCase().includes(term)
  );
}

