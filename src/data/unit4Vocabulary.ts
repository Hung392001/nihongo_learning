/**
 * Helper function to get vocabulary by category
 */
export function getUnit4VocabularyByCategory(category: string): typeof unit4Vocabulary {
  return unit4Vocabulary.filter(item => item.category === category);
}

/**
 * Helper function to get vocabulary by tag
 */
export function getUnit4VocabularyByTag(tag: string): typeof unit4Vocabulary {
  return unit4Vocabulary.filter(item => item.tags?.includes(tag));
}

/**
 * Helper function to search vocabulary
 */
export function searchUnit4Vocabulary(searchTerm: string): typeof unit4Vocabulary {
  const term = searchTerm.toLowerCase();
  return unit4Vocabulary.filter(item =>
    item.hiragana.includes(term) ||
    item.kanji?.includes(term) ||
    item.vietnamese.toLowerCase().includes(term)
  );
}

